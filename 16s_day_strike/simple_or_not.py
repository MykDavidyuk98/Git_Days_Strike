def simple_or_not (x: int):
    """ –ü–µ—Ä–µ–≤—ñ—Ä—è—î —á–∏ —î —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–∏–º(–ø—Ä–æ—Å—Ç–µ / –Ω–µ –ø—Ä–æ—Å—Ç–µ),
        –ß–∏ –¥—ñ–ª–∏—Ç—å—Å—è –≤–æ–Ω–æ —Å–∞–º–µ –Ω–∞ —Å–µ–±–µ —Ç–∞ –Ω–∞ 1"""
    if x <= 1:
        return '–Ω–µ –ø—Ä–æ—Å—Ç–µ'
    for i in range(2, int(x**0.5)+1):
        if x % i == 0:
            return '–Ω–µ –ø—Ä–æ—Å—Ç–µ'
    return '–ø—Ä–æ—Å—Ç–µ'
